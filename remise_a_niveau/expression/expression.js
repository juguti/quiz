// Fonction pour générer l'affichage
function afficherDictionnaire(dictionnaire) {
    const body = document.body;
    let colorIndex = 0;
    const colors = ["#FF6347", "#4682B4", "#32CD32", "#FFD700", "#8A2BE2", "#DC143C", "#FF4500"];

    // Parcourir chaque clé du dictionnaire principal
    for (const [cle, sousDictionnaire] of Object.entries(dictionnaire)) {
        // Créer et afficher la clé comme h1
        const h1 = document.createElement('h1');
        h1.textContent = cle;
        body.appendChild(h1);

        // Créer une liste pour les sous-éléments
        const ul = document.createElement('ul');
        ul.style.backgroundColor = colors[colorIndex % colors.length]+"7f";
        ul.style.padding = "16px"
        ul.style.borderRadius = "16px"
        body.appendChild(ul);

        // Variable pour générer des couleurs de fond
        

        // Parcourir chaque sous-clé du dictionnaire secondaire
        for (const [sousCle, valeurs] of Object.entries(sousDictionnaire)) {
            // Créer un élément de liste (li) pour chaque valeur
            const li = document.createElement('li');
            li.textContent = `${sousCle}: ${valeurs}`;
            // Appliquer une couleur de fond différente à chaque élément
            ul.appendChild(li);
            
            // Incrémenter l'indice de la couleur pour le prochain élément
            
        }
        colorIndex++;
    }
}

// Appeler la fonction pour afficher le dictionnaire
afficherDictionnaire(CH1);

